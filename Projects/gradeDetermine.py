marks=int(input("Enter your marks: "))
if marks>=90:
    print("Grade is A+")
elif marks>=80:
    print("Grade is A")
elif marks>=70:
    print("Grade is B+")
elif marks>=60:
    print("Grade is B")
elif marks>=50:
    print("Grade is C+")
elif marks>=40:
    print("Grade is C")
else:
    print("Grade is F")
    